package com.ieschabas;

/**
 * Clase Mayores donde estableceremos que el programa nos analice los datos A y B introducidos en Main para que luego nos muestre en pantalla cuál de los dos es mayor.
 * Para ello utilizamos la clase Math que tiene un método "max" que analiza los datos y devuelve el mayor
 * @author Pedro Enrique Torres Sesé
 * @version 1.0
 */

public class Mayores {

    public int max2(int a, int b) {
        return Math.max(a, b);
    }
}
